from functools import partial


def prepare_person(dataset, input_column, prompt=""):
    x = dataset[input_column]
    if len(prompt):
        for i in range(len(x)):
            x[i] = prompt.format(text=x[i])
    y = []
    for _ in x:
        y.append("")
    return x, y


